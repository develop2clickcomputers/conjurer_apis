package com.pisight.pimoney.dto.online;

public class Response {

	private String id = null;

	private String timestamp = null;

	private String accessCode = null;
	
	private String instCode = null;
	
	private String dflUname = null;
	private String dflPass = null;
	private String dflHost = null;
	private String dfl = null;

	private String xml = null;
	
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the timestamp
	 */
	public String getTimestamp() {
		return timestamp;
	}

	/**
	 * @param timestamp the timestamp to set
	 */
	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	/**
	 * @return the accessCode
	 */
	public String getAccessCode() {
		return accessCode;
	}

	/**
	 * @param accessCode the accessCode to set
	 */
	public void setAccessCode(String accessCode) {
		this.accessCode = accessCode;
	}

	/**
	 * @return the instCode
	 */
	public String getInstCode() {
		return instCode;
	}

	/**
	 * @param instCode the instCode to set
	 */
	public void setInstCode(String instCode) {
		this.instCode = instCode;
	}
	

	/**
	 * @return the xml
	 */
	public String getXml() {
		return xml;
	}

	/**
	 * @param xml the xml to set
	 */
	public void setXml(String xml) {
		this.xml = xml;
	}

	/**
	 * @return the dflUname
	 */
	public String getDflUname() {
		return dflUname;
	}

	/**
	 * @param dflUname the dflUname to set
	 */
	public void setDflUname(String dflUname) {
		this.dflUname = dflUname;
	}

	/**
	 * @return the dflPass
	 */
	public String getDflPass() {
		return dflPass;
	}

	/**
	 * @param dflPass the dflPass to set
	 */
	public void setDflPass(String dflPass) {
		this.dflPass = dflPass;
	}

	/**
	 * @return the dflHost
	 */
	public String getDflHost() {
		return dflHost;
	}

	/**
	 * @param dflHost the dflHost to set
	 */
	public void setDflHost(String dflHost) {
		this.dflHost = dflHost;
	}

	/**
	 * @return the dfl
	 */
	public String getDfl() {
		return dfl;
	}

	/**
	 * @param dfl the dfl to set
	 */
	public void setDfl(String dfl) {
		this.dfl = dfl;
	}
  
}
